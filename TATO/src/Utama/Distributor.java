    public void autoIn() {
        try {
            Class.forName(driver);
            con = DriverManager.getConnection(url, user, pwd);

            String sqlquery = "SELECT MAX(RIGHT(id_distributor,4)) AS no_auto FROM distributor";
            PreparedStatement st = con.prepareStatement(sqlquery);
            ResultSet rs = st.executeQuery();

            String no_a, no_p = "";
            int p;
            if (rs.next()) {
                no_a = Integer.toString(rs.getInt(1)+1);
                p = no_a.length();
                
                for(int i = 1; i <=4-p;i++){
                no_p = no_p + "0";
                }
                Field_Tambah_Kode_Distributor.setText("DST"+no_p+no_a);
            }
                

        } catch (Exception e) {
            Field_Tambah_Kode_Distributor.setText("");
            e.printStackTrace();
        }

    }
    
    

