    private void Btn_Tambah_Tambah_DistributorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_Tambah_Tambah_DistributorActionPerformed
        String id = Field_Tambah_Kode_Distributor.getText();
        String nama = Field_Tambah_Nama_Distributor.getText();
        String alamat = Field_Tambah_Alamat_Distributor.getText();
        String kota = Field_Tambah_Kota_Distributor.getText();
        String telepon = Field_Tambah_Telepon_Distributor.getText();
        String email = Field_Tambah_Email_Distributor.getText();
        SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
        String bergabung = date.format(Field_Tambah_Bergabung_Distributor.getDate());
        String status = String.valueOf(Field_Tambah_Status_Distributor.getSelectedItem());
        String no_rek = Field_Tambah_Rekening_Distributor.getText();
        String bank = Field_Tambah_Bank_Distributor.getText();
        String ket = Field_Tambah_Keterangan_Distributor.getText();

        try {
            db.aksi("INSERT INTO distributor VALUES ('" + id + "','" + nama + "','" + kota + "','" + bergabung + "','" + telepon + "','" + email + "','" + alamat + "','" + no_rek + "','" + bank + "','" + ket + "','" + status + "')");
            model.setRowCount(0);
            Tbl_Daftar_Distributor.setModel(model);
          //  Tbl_Detail_Distributor.setModel(model);
            Tbl_Update_Distributor.setModel(model);
            getDataD();
            autoIn();
            JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");
            pn_Konten_Distributor.removeAll();
            pn_Konten_Distributor.repaint();
            pn_Konten_Distributor.revalidate();

            pn_Konten_Distributor.add(pn__Distributor_Utama);
            pn_Konten_Distributor.repaint();
            pn_Konten_Distributor.revalidate();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan: " + e.getMessage());
        }

    }//GEN-LAST:event_Btn_Tambah_Tambah_DistributorActionPerformed

